/**
 * Shared Connection Type Configuration
 * Single source of truth for all MCP connection types displayed in the UI.
 * All pages import from here to avoid duplicated lists.
 */

export type FieldType =
  | 'text'
  | 'password'
  | 'url'
  | 'number'
  | 'select'
  | 'toggle'
  | 'file'
  | 'textarea';

export interface ConnectionField {
  name: string;
  key: string;
  type: FieldType;
  placeholder?: string;
  required?: boolean;
  options?: string[];
  hint?: string;
  defaultValue?: string | number;
}

export interface ConnectionTypeConfig {
  icon: string;
  /** Path to a static logo image (relative to public root, e.g. /legacy/mysql.svg).
   *  When present, rendered instead of the emoji icon. */
  logo?: string;
  name: string;
  description: string;
  color: string;
  bgColor: string;
  borderColor: string;
  category: ConnectionCategory;
  /** Per-software connection fields. When absent, falls back to generic Host/Port/DB/User/Pass. */
  fields?: ConnectionField[];
}

